package main

import "fmt"

func main() {
  fmt.Println("Println() can be different than Print().")
  fmt.Println("Here's an example.")
  fmt.Println("------------------------------")
  fmt.Println("Println():")
  fmt.Println("Greg is very skibidi")
  fmt.Println("Print():")
  fmt.Print("I", "am", "skibidi\n")
  fmt.Println("------------------------------")
  fmt.Println("Notice the spacing? It happens when you do put commas after each text. You can still use Print like normal tho!")
  fmt.Println("------------------------------")
  fmt.Print("greg is very skibidi\n")
  fmt.Println("that was a Print()!!!")

}
